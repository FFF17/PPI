<script>
    function login() {
        Swal.fire({
            html: 'Loading . . .',
            allowOutsideClick:false,
            onBeforeOpen: function() {
                Swal.showLoading()
            }
        });
        var username = $("#username").val();
        var password = $("#password").val();
        $.ajax({
            method: "POST",
            url: "<?php echo site_url()?>/login/submit",
            data: { username: username, password: password }
        }).done(function (msg) {
            Swal.close();
            msg = msg.trim();
            if (msg != "") {
                $.notify({
                    icon: "add_alert",
                    message: "Warning, <b>"+msg+"</b>"

                },{
                    type: 'danger',
                    timer: 4000,
                    placement: {
                        from: "top",
                        align: "right"
                    }
                });
            } else {
                location.reload();
            }
        });
    }
</script>
